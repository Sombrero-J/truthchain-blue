<section>
	<header>
		<h2>Truthchain Guidelines for Submission</h2>
		<p>
			In order to maintain the integrity and reliability of the information shared on this platform,
			all users are required to adhere to the following rules when submitting content. Our goal is
			to build a community-driven system that values <em>truth</em>, <strong>transparency</strong>,
			and <strong>verification</strong> above all else.
		</p>
	</header>
	{#if open}
	<main>
		<section>
			<h2>1. Verifiability is Mandatory</h2>
			<p>
				All submissions must contain <strong>verifiable facts</strong>. A verifiable fact is any
				statement that can be independently confirmed through credible and recognized sources. To
				ensure your post meets this standard, you <strong>must provide at least one</strong> of the following
				supporting materials:
			</p>
			<ul>
				<li>
					<strong>Primary Evidence</strong>: Upload a video, image, or document that provides direct
					evidence of the event or claim. Ensure the media is clear, original (not manipulated), and
					accurately reflects the claim.
				</li>
				<li>
					<strong>Cited Sources</strong>: Provide
					<strong>reliable and authoritative sources</strong> (e.g., news organizations, academic institutions,
					or government records) that directly support the fact being presented. Ensure all links are
					functional and direct users to the relevant information.
				</li>
			</ul>
			<p>Acceptable sources include, but are not limited to:</p>
			<ul>
				<li>Peer-reviewed academic journals</li>
				<li>Established news agencies (e.g., BBC, Reuters, Associated Press)</li>
				<li>Official government publications or databases</li>
				<li>Reports from verified organizations (e.g., UN, WHO, World Bank)</li>
			</ul>
			<p>Unacceptable sources include:</p>
			<ul>
				<li>
					Personal blogs, social media posts, or opinion pieces (unless posted by a verified expert)
				</li>
				<li>Websites without clear attribution or editorial standards</li>
				<li>Paid or sponsored content lacking independent verification</li>
			</ul>
		</section>

		<section>
			<h2>2. No Subjective Claims, Opinions, or Personal Feelings</h2>
			<p>
				This platform is strictly for the validation of <strong>objective facts</strong>. Therefore,
				submissions that contain
				<strong>subjective thoughts, personal opinions, or emotional appeals</strong> will not be accepted.
			</p>
			<p><strong>Subjective</strong> content includes:</p>
			<ul>
				<li>
					Personal beliefs (e.g., “I think this is wrong” or “In my opinion, this should happen”)
				</li>
				<li>Emotional reactions (e.g., “This is horrible!” or “I feel outraged about this”)</li>
				<li>Unsubstantiated conjecture (e.g., “It seems like…” or “This could be true”)</li>
			</ul>
			<p>
				All posts must maintain a neutral and factual tone. The content should be presented <strong
					>without bias</strong
				>, focusing solely on <strong>what can be proven</strong>.
			</p>
		</section>

		<section>
			<h2>3. Context is Crucial</h2>
			<p>
				To ensure that the facts presented are properly understood, all claims must be provided <strong
					>in full context</strong
				>. Any attempt to mislead, cherry-pick information, or present facts out of context will
				result in rejection of the submission.
			</p>
			<ul>
				<li>
					When presenting data or statistics, always include the source and the circumstances under
					which the data was collected (e.g., time period, location, methodology).
				</li>
				<li>
					Do not isolate quotes, figures, or events from their larger narrative in a way that
					distorts the meaning.
				</li>
			</ul>
		</section>

		<section>
			<h2>4. Avoid Ambiguity: Be Precise and Specific</h2>
			<p>
				All content must be <strong>clear, precise, and specific</strong>. Ambiguous statements or
				claims that can be interpreted in multiple ways are not acceptable. Provide enough detail so
				that others can fully understand and verify the fact.
			</p>
			<ul>
				<li>
					Avoid vague language such as “many people say” or “it’s well known that…”. Instead,
					specify <strong>who</strong>, <strong>what</strong>, <strong>when</strong>,
					<strong>where</strong>, and <strong>how</strong>.
				</li>
				<li>
					If discussing a trend or a group, provide exact numbers or names where applicable (e.g.,
					“70% of respondents” instead of “many people”).
				</li>
			</ul>
		</section>

		<section>
			<h2>5. No Unverified Claims of Conspiracy or Misinformation</h2>
			<p>
				Posts that promote <strong
					>conspiracy theories, misinformation, or unsubstantiated accusations</strong
				>
				will not be tolerated. All claims must be backed by credible evidence. Content that is
				designed to
				<strong>intentionally mislead, distort facts, or discredit legitimate information</strong> will
				be removed.
			</p>
		</section>

		<section>
			<h2>6. Original Content and Attribution</h2>
			<p>
				All content uploaded to the platform must either be <strong>original</strong> or properly
				<strong>attributed</strong>. Plagiarism is strictly prohibited.
			</p>
			<ul>
				<li>
					If you are using media (e.g., videos, images, or text) that you did not create, you must
					provide <strong>clear attribution</strong> to the original source.
				</li>
				<li>
					When quoting or referencing an external source, provide the necessary citation, including
					the author's name, publication date, and direct link.
				</li>
			</ul>
		</section>

		<section>
			<h2>7. Relevance to Public Discourse</h2>
			<p>
				All submissions must contribute to <strong>public discourse</strong> by presenting facts
				that are <strong>relevant, timely, and of public interest</strong>. Avoid trivial or
				inconsequential facts that do not add value to the conversation or are not relevant to the
				community.
			</p>
		</section>

		<section>
			<h2>8. Full Disclosure of Potential Biases</h2>
			<p>
				If you are submitting facts on a topic in which you have a personal or financial interest, <strong
					>full disclosure is required</strong
				>. This is to ensure transparency and allow the community to evaluate your submission
				without bias.
			</p>
			<ul>
				<li>
					Declare any potential conflicts of interest (e.g., affiliations with organizations
					mentioned in the post, sponsorships, or any direct involvement in the event being
					reported).
				</li>
			</ul>
		</section>

		<section>
			<h2>9. Consequences of Misleading or False Submissions</h2>
			<p>
				Users found submitting <strong
					>intentionally misleading, false, or manipulated content</strong
				> will face consequences, including but not limited to:
			</p>
			<ul>
				<li>Immediate removal of the post</li>
				<li>Temporary or permanent suspension from the platform</li>
				<li>A reduction in your <strong>credibility score</strong></li>
			</ul>
			<p>
				In cases of repeated violations, users will be <strong>permanently banned</strong> from submitting
				content.
			</p>
		</section>

		<section>
			<h2>10. Community-Driven Validation</h2>
			<p>
				Once submitted, your content will be reviewed by the community and subject to <strong
					>community voting</strong
				>. You are encouraged to engage constructively with the feedback provided and offer
				additional clarification if necessary. Keep in mind:
			</p>
			<ul>
				<li>
					Content that receives validation based on credible evidence will be marked as <strong
						>“Verified”</strong
					>.
				</li>
				<li>
					Content that cannot be verified will either be marked as <strong>“Unverifiable”</strong>
					or <strong>“False”</strong>, depending on the outcome of the community review.
				</li>
			</ul>
		</section>
	</main>

